    completeDeck = gryffindor_cards + hufflepuff_cards + ravenclaw_cards + slytherin_cards
    newDeck = []
    for i in range(0, 11):
        newDeck.append(completeDeck.pop(random.randint(0, len(completeDeck)-1)))
    return newDeck
       
       
