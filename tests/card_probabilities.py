def getHouseProbabilities(player_cards):
    gryffindor_count = sum(1 for card in player_cards if card["house"] == "gryffindor")
    hufflepuff_count = sum(1 for card in player_cards if card["house"] == "hufflepuff")
    ravenclaw_count = sum(1 for card in player_cards if card["house"] == "ravenclaw")
    slytherin_count = sum(1 for card in player_cards if card["house"] == "slytherin")
    total_cards = len(player_cards)
    return {
        "gryffindor": (gryffindor_count / total_cards) * 100,
        "hufflepuff": (hufflepuff_count / total_cards) * 100,
        "ravenclaw": (ravenclaw_count / total_cards) * 100,
        "slytherin": (slytherin_count / total_cards) * 100,
    }
    

def printPlayerCards(player_name, cards):
    width = 100
    
    print()
    print(player_name.center(width))
    print("═" * width)

    print("║ %-14s ║ %-10s ║ %-6s ║ %-10s ║ %-44s ║" % (
        "House", "Name", "Value", "Type", "Image"
    ))

    print("═" * width)

    for card in cards:
        print("║ %-14s ║ %-10s ║ %-6s ║ %-10s ║ %-44s ║" % (
            card["house"],
            card["name"],
            card["value"],
            card["type"],
            card["src"]
        ))

    print("═" * width)


printPlayerCards("Player A", player_A_cards)
